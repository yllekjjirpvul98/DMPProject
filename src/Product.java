public class Product {

    String type;
    String index;
    String retail;
    String discount;
    String wholesale;
    String quantity;
    String total;

    Product(String type, String index, String retail, String discount, String wholesale, String quantity, String total){

        this.type = type;
        this.discount = discount;
        this.index = index;
        this.retail = retail;
        this.wholesale = wholesale;
        this.quantity = quantity;
        this.total = total;

    }
}
